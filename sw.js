self.addEventListener('install', (e) => {
e.waitUntil(
caches.open('assistant-store').then((cache) => cache.addAll([
'index.html',
'assistant.html',
'manifest.json'
])),
);
});

self.addEventListener('fetch', (e) => {
e.respondWith(
caches.match(e.request).then((response) => response || fetch(e.request)),
);
});
